var lastText = "View Grocery List";

function convertToId(smoothieName){
    smoothieName = smoothieName.replace(/ /g, ""); 
    smoothieName = smoothieName.charAt(0).toLowerCase() + smoothieName.substr(1); 
    return smoothieName;
}

//function for when a smoothie link is clicked in the sidenav. This function hides the recipe block that was previously displayed and makes the new one visible. The 'id' paremeter is a linkId, for example "greenSmoothieLink"
function navSmoothieLinkClicked(id){
    document.getElementById('getStarted').setAttribute('class', "hide"); 
    if (lastClickedId !== ""){ 
        document.getElementById(lastClickedId).style.visibility="hidden";
        document.getElementById(lastClickedId).style.display="none";
        document.getElementById(lastClickedId+"Link").classList.remove("lastClicked");
    }
    var recipeId = id.slice(0, -4); 
    lastClickedId = recipeId; 
    document.getElementById(recipeId+"Link").setAttribute('class', "lastClicked");
    document.getElementById('groceryList').style.visibility = "hidden";
    document.getElementById('groceryList').style.display = "none";
    document.getElementById("recipeDiv").style.visibility="visible";
    document.getElementById("recipeDiv").style.display = null;
    document.getElementById(recipeId).style.visibility="visible";
    document.getElementById(recipeId).style.display="block";
    document.getElementById('viewGroceryListLink').innerHTML = "View Grocery List";
    lastText = "View Grocery List";
    
}

